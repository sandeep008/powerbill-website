import type { ReactNode } from "react";
import { useInView } from "../hooks/useInView";

/**
 * Fade+slide-up wrapper for one-shot scroll-entrance animation. Respects
 * prefers-reduced-motion via the `motion-reduce:` variants below -- content
 * just appears immediately for those users instead of transitioning in.
 */
export function Reveal({
  children,
  delay = 0,
  className = "",
}: {
  children: ReactNode;
  delay?: number;
  className?: string;
}) {
  const { ref, inView } = useInView<HTMLDivElement>();

  return (
    <div
      ref={ref}
      className={`transition-all duration-700 ease-out motion-reduce:transition-none motion-reduce:opacity-100 motion-reduce:translate-y-0 ${
        inView ? "opacity-100 translate-y-0" : "opacity-0 translate-y-6"
      } ${className}`}
      style={{ transitionDelay: inView ? `${delay}ms` : "0ms" }}
    >
      {children}
    </div>
  );
}
